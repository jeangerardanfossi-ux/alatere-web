/**
 * Emplacement d'image. Si `src` est fourni, affiche la photo (object-fit cover) ;
 * sinon, montre l'état vide (cadre pointillé + légende) du prototype.
 */
export function ImageSlot({
  variant = '',
  cap,
  className = '',
  src,
  objectPosition,
}: {
  variant?: string;
  cap: string;
  className?: string;
  src?: string;
  /** Cadrage de la photo, ex. "50% 15%" pour montrer le haut (têtes). */
  objectPosition?: string;
}) {
  const cls = [
    'g-img-slot',
    variant ? `g-img-slot--${variant}` : '',
    src ? 'g-img-slot--filled' : '',
    className,
  ]
    .filter(Boolean)
    .join(' ');
  return (
    <div className={cls} role="img" aria-label={cap}>
      {src ? (
        // eslint-disable-next-line @next/next/no-img-element
        <img src={src} alt={cap} loading="lazy" style={objectPosition ? { objectPosition } : undefined} />
      ) : (
        <>
          <Icon name="image" />
          <span className="g-img-slot__cap">{cap}</span>
        </>
      )}
    </div>
  );
}
